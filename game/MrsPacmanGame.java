package com.sbmaster.spring.couples.game;

public class MrsPacmanGame extends Game {
	@Override
	public void up() {
		System.out.println("Mrs. Pacman up");
	}

	@Override
	public void down() {
		System.out.println("Mrs. Pacman down");
	}

	@Override
	public void left() {
		System.out.println("Mrs. Pacman left");
	}

	@Override
	public void right() {
		System.out.println("Mrs. Pacman right");
	}

}
